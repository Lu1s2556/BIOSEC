
package Modelo;


public class EXAMENES {
    private int ID_PRUEBA;
    private String NOMBRE;

    public EXAMENES(int ID_PRUEBA, String NOMBRE) {
        this.ID_PRUEBA = ID_PRUEBA;
        this.NOMBRE = NOMBRE;
    }

    public int getID_PRUEBA() {
        return ID_PRUEBA;
    }

    public String getNOMBRE() {
        return NOMBRE;
    }
    
    
    
    
}
